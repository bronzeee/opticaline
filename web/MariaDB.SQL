CREATE TABLE `USER` (
  `ID`         INT(8)   NOT NULL AUTO_INCREMENT,
  `USER_NAME`  TEXT     NOT NULL,
  `E_MAIL`     TEXT     NOT NULL,
  `PASSWORD`   TEXT     NOT NULL,
  `NAME`       TEXT     NULL,
  `SEX`        INT(1)   NULL DEFAULT '1',
  `BIRTHDAY`   DATETIME NULL DEFAULT NULL,
  `LAST_LOGIN` DATETIME NOT NULL,
  PRIMARY KEY (`ID`)
)
  COLLATE ='utf8_bin'
  ENGINE =InnoDB
  AUTO_INCREMENT =1001;


