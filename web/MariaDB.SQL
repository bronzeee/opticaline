CREATE TABLE `BLOG` (
  `ID`          INT(8)   NOT NULL AUTO_INCREMENT,
  `USER_ID`     INT(8)   NOT NULL,
  `TITLE`       TEXT     NOT NULL,
  `CONTENT`     TEXT     NULL DEFAULT NULL,
  `CREATE_DATE` DATETIME NULL DEFAULT NULL,
  `EDIT_DATE`   DATETIME NULL DEFAULT NULL,
  `TAG_ID`      TEXT     NULL DEFAULT NULL, FOREIGN KEY (`USER_ID`) REFERENCES `USER` (`ID`)
  ON
  UPDATE NO ACTION
  ON
  DELETE NO ACTION, PRIMARY KEY (`ID`)
)
  COLLATE ='utf8_bin'
  ENGINE =InnoDB
  AUTO_INCREMENT =10000;

